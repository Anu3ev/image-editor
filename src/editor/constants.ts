/**
 * Абсолютное значение диапазона зума для плавного центрирования viewport.
 * Центрирование будет активно в диапазоне [defaultZoom, defaultZoom + CENTERING_RANGE]
 *
 * Физический смысл: насколько далеко от defaultZoom можно отдалиться, чтобы центрирование ещё работало.
 *
 * Рекомендуемый диапазон: 0.05 - 0.5
 * - 0.05 = очень узкая зона, центрирование работает только у самого defaultZoom
 * - 0.2 = средняя зона (текущее значение, сбалансированное)
 * - 0.5 = широкая зона, центрирование работает дольше при отдалении
 *
 * Пример при defaultZoom=0.118:
 * - VIEWPORT_CENTERING_RANGE=0.05: центрирование от 0.118 до 0.168
 * - VIEWPORT_CENTERING_RANGE=0.20: центрирование от 0.118 до 0.318 (текущее)
 * - VIEWPORT_CENTERING_RANGE=0.50: центрирование от 0.118 до 0.618
*/
export const VIEWPORT_CENTERING_RANGE = 0.2

/**
 * Множитель для зоны плавного ослабления ограничений перетаскивания перед центрированием.
 * Зона ослабления = VIEWPORT_CENTERING_RANGE * CONSTRAINT_FADE_MULTIPLIER
 *
 * Физический смысл: насколько плавно отключаются ограничения перетаскивания при приближении к зоне центрирования.
 *
 * Рекомендуемый диапазон: 1 - 4
 * - 1 = резкое отключение, ограничения исчезают сразу на границе зоны центрирования
 * - 2 = плавное отключение (текущее значение, комфортный переход)
 * - 4 = очень плавное, ограничения начинают ослабляться заранее
 *
 * Пример при VIEWPORT_CENTERING_RANGE = 0.2 и defaultZoom=0.118:
 * - CONSTRAINT_FADE_MULTIPLIER = 1: зона ослабления от 0.318 до 0.518
 * - CONSTRAINT_FADE_MULTIPLIER = 2: зона ослабления от 0.318 до 0.718 (текущее)
 * - CONSTRAINT_FADE_MULTIPLIER = 4: зона ослабления от 0.318 до 1.118
 */
export const CONSTRAINT_FADE_MULTIPLIER = 2

/**
 * Скорость адаптивного центрирования при появлении пустого пространства (в процентах).
 * Значение от 0 до 100.
 *
 * Физический смысл: какой процент пути до центра проходится за один шаг зума.
 *
 * Диапазон: 0 - 100
 * - 0 = нет центрирования
 * - 1-2 = очень медленное центрирование, едва заметное
 * - 3-5 = мягкое центрирование (рекомендуется для комфортной работы)
 * - 10 = среднее центрирование
 * - 20+ = быстрое центрирование, может быть заметен рывок
 *
 * Примеры:
 * - ADAPTIVE_CENTERING_SPEED = 3: за каждый шаг зума viewport смещается на 3% ближе к центру
 * - ADAPTIVE_CENTERING_SPEED = 10: за 10 шагов зума viewport пройдёт ~65% пути к центру
 * - ADAPTIVE_CENTERING_SPEED = 50: за 2 шага зума viewport почти полностью центрируется
 */
export const ADAPTIVE_CENTERING_SPEED = 5

/**
 * Множитель ускорения центрирования при увеличении пустого пространства.
 * Значение от 1.0 до 10.0.
 *
 * Физический смысл: во сколько раз увеличивается скорость центрирования,
 * когда пустое пространство занимает 100% вьюпорта.
 *
 * Диапазон: 1.0 - 10.0
 * - 1.0 = нет ускорения, скорость всегда постоянная
 * - 1.5 = небольшое ускорение (50% прирост скорости)
 * - 2.0 = умеренное ускорение (рекомендуется, в 2 раза быстрее)
 * - 5.0 = сильное ускорение (в 5 раз быстрее)
 * - 10.0 = очень сильное ускорение, "магнитная" подтяжка к центру
 *
 * Примеры при ADAPTIVE_CENTERING_SPEED = 3:
 * - ADAPTIVE_CENTERING_ACCELERATION = 1.0: скорость всегда 3%, не зависит от пустого пространства
 * - ADAPTIVE_CENTERING_ACCELERATION = 2.0: скорость от 3% (нет пустоты) до 6% (полный viewport пустоты)
 * - ADAPTIVE_CENTERING_ACCELERATION = 5.0: скорость от 3% (нет пустоты) до 15% (полный viewport пустоты)
 *
 * Формула: finalSpeed = ADAPTIVE_CENTERING_SPEED × (1 + (ADAPTIVE_CENTERING_ACCELERATION - 1) × emptySpaceRatio²)
 */
export const ADAPTIVE_CENTERING_ACCELERATION = 2.0
