/**
 * Абсолютное значение диапазона зума для плавного центрирования viewport.
 * Центрирование будет активно в диапазоне [defaultZoom, defaultZoom + CENTERING_RANGE]
 *
 * Физический смысл: насколько далеко от defaultZoom можно отдалиться, чтобы центрирование ещё работало.
 *
 * Рекомендуемый диапазон: 0.05 - 0.5
 * - 0.05 = очень узкая зона, центрирование работает только у самого defaultZoom
 * - 0.2 = средняя зона (текущее значение, сбалансированное)
 * - 0.5 = широкая зона, центрирование работает дольше при отдалении
 *
 * Пример при defaultZoom=0.118:
 * - CENTERING_RANGE=0.05: центрирование от 0.118 до 0.168
 * - CENTERING_RANGE=0.20: центрирование от 0.118 до 0.318 (текущее)
 * - CENTERING_RANGE=0.50: центрирование от 0.118 до 0.618
*/
export const VIEWPORT_CENTERING_RANGE = 0.2

/**
 * Множитель для зоны плавного ослабления ограничений перетаскивания перед центрированием.
 * Зона ослабления = VIEWPORT_CENTERING_RANGE * CONSTRAINT_FADE_MULTIPLIER
 *
 * Физический смысл: насколько плавно отключаются ограничения перетаскивания при приближении к зоне центрирования.
 *
 * Рекомендуемый диапазон: 1 - 4
 * - 1 = резкое отключение, ограничения исчезают сразу на границе зоны центрирования
 * - 2 = плавное отключение (текущее значение, комфортный переход)
 * - 4 = очень плавное, ограничения начинают ослабляться заранее
 *
 * Пример при CENTERING_RANGE=0.2 и defaultZoom=0.118:
 * - Multiplier=1: зона ослабления от 0.318 до 0.518
 * - Multiplier=2: зона ослабления от 0.318 до 0.718 (текущее)
 * - Multiplier=4: зона ослабления от 0.318 до 1.118
 */
export const CONSTRAINT_FADE_MULTIPLIER = 2

/**
 * Базовая сила адаптивного центрирования при zoom-out.
 * Применяется когда только-только появляется пустое пространство (серый фон).
 *
 * Физический смысл: какой % от расстояния до центра "проходится" за один шаг zoom-out.
 * Значение 0.03 означает, что за каждый шаг зума viewport смещается на 3% ближе к центру.
 *
 * Рекомендуемый диапазон: 0.01 - 0.1
 * - 0.01 = очень медленное центрирование, едва заметное
 * - 0.03 = мягкое центрирование (текущее, плавное и комфортное)
 * - 0.05 = среднее центрирование
 * - 0.1 = быстрое центрирование, может быть заметен рывок
 *
 * Важно: должно быть меньше ADAPTIVE_CENTERING_MAX_STRENGTH
 */
export const ADAPTIVE_CENTERING_BASE_STRENGTH = 0.03

/**
 * Максимальная сила адаптивного центрирования при zoom-out.
 * Применяется когда видно много пустого пространства вокруг монтажной области.
 *
 * Физический смысл: какой % от расстояния до центра "проходится" за один шаг zoom-out
 * когда пустое пространство занимает ~100% ширины/высоты viewport.
 *
 * Рекомендуемый диапазон: 0.03 - 0.2
 * - 0.03 = не усиливается (если равно BASE_STRENGTH)
 * - 0.05 = умеренное усиление (текущее, плавное ускорение)
 * - 0.1 = заметное усиление
 * - 0.2 = сильное усиление, быстрая "магнитная" подтяжка к центру
 *
 * Важно: должно быть больше или равно ADAPTIVE_CENTERING_BASE_STRENGTH
 *
 * Формула усиления: strength = BASE + (MAX - BASE) × (emptySpaceRatio²)
 * где emptySpaceRatio от 0 (нет пустого пространства) до ~1 (много пустого пространства)
 */
export const ADAPTIVE_CENTERING_MAX_STRENGTH = 0.05
